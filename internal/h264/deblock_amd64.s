// func h264VLoopFilterLuma8ASM(pix *uint8, stride int, alpha int32, beta int32, tc0 *int8)
TEXT ·h264VLoopFilterLuma8ASM(SB), NOSPLIT, $0-32
	MOVQ pix+0(FP), DI
	MOVQ stride+8(FP), SI
	MOVL alpha+16(FP), AX
	MOVL beta+20(FP), BX
	MOVQ tc0+24(FP), CX
	JMP  ·h264Luma8SSE2Core(SB)

// h264Luma8SSE2Core filters 16 8-bit samples with DI=pix, SI=stride,
// AX=alpha, BX=beta, and CX=tc0.
TEXT ·h264Luma8SSE2Core(SB), NOSPLIT, $0-0
	TESTL AX, AX
	JLE   luma8_sse2_ret
	TESTL BX, BX
	JLE   luma8_sse2_ret
	DECL AX
	DECL BX
	MOVQ SI, DX
	LEAQ (SI)(SI*2), R8
	MOVQ DI, R9
	SUBQ R8, R9

	MOVOU (R9)(SI*1), X0   // p1
	MOVOU (R9)(SI*2), X1   // p0
	MOVOU (DI), X2         // q0
	MOVOU (DI)(SI*1), X3   // q1

	MOVD AX, X4
	PSHUFLW $0, X4, X4
	PUNPCKLQDQ X4, X4
	PACKUSWB X4, X4        // alpha - 1
	MOVD BX, X5
	PSHUFLW $0, X5, X5
	PUNPCKLQDQ X5, X5
	PACKUSWB X5, X5        // beta - 1

	MOVOU X2, X6
	MOVOU X1, X7
	PSUBUSB X1, X6
	PSUBUSB X2, X7
	POR X6, X7
	PSUBUSB X4, X7

	MOVOU X1, X6
	MOVOU X0, X4
	PSUBUSB X0, X6
	PSUBUSB X1, X4
	POR X6, X4
	PSUBUSB X5, X4
	POR X4, X7

	MOVOU X2, X6
	MOVOU X3, X4
	PSUBUSB X3, X6
	PSUBUSB X2, X4
	POR X6, X4
	PSUBUSB X5, X4
	POR X4, X7
	PXOR X6, X6
	PCMPEQB X6, X7        // base mask

	MOVD (CX), X8
	PUNPCKLBW X8, X8
	PUNPCKLBW X8, X8
	MOVOU X8, X6
	PCMPEQB X9, X9
	PCMPGTB X9, X6        // tc0 >= 0
	PAND X7, X6           // base mask with inactive tc0 lanes cleared
	MOVOU X6, X9
	PAND X9, X8           // masked tc0

	MOVOU (R9), X3        // p2
	MOVOU X3, X7
	MOVOU X1, X6
	PSUBUSB X1, X7
	PSUBUSB X3, X6
	PSUBUSB X5, X7
	PSUBUSB X5, X6
	PCMPEQB X7, X6
	PAND X9, X6           // |p2-p0| < beta and base mask
	MOVOU X8, X7
	PSUBB X6, X7          // tc + p-side increment
	PAND X8, X6           // p1 clip tc

	MOVOU X1, X4
	PAVGB X2, X4
	PAVGB X4, X3
	MOVOU (R9), X10
	PXOR X10, X4
	PAND ·h264DeblockPB1(SB), X4
	PSUBUSB X4, X3
	MOVOU X0, X10
	PSUBUSB X6, X10
	PADDUSB X0, X6
	PMAXUB X10, X3
	PMINUB X6, X3
	MOVOU X3, (R9)(SI*1)

	MOVOU (DI)(SI*2), X4  // q2
	MOVOU X4, X3
	MOVOU X2, X6
	PSUBUSB X2, X3
	PSUBUSB X4, X6
	PSUBUSB X5, X3
	PSUBUSB X5, X6
	PCMPEQB X3, X6
	PAND X9, X6           // |q2-q0| < beta and base mask
	PAND X6, X8           // q1 clip tc
	PSUBB X6, X7          // final tc
	MOVOU (DI)(SI*1), X3  // q1

	MOVOU X1, X6
	PAVGB X2, X6
	PAVGB X6, X4
	MOVOU (DI)(SI*2), X10
	PXOR X10, X6
	PAND ·h264DeblockPB1(SB), X6
	PSUBUSB X6, X4
	MOVOU X3, X10
	PSUBUSB X8, X10
	PADDUSB X3, X8
	PMAXUB X10, X4
	PMINUB X8, X4
	MOVOU X4, (DI)(SI*1)

	PCMPEQB X4, X4
	MOVOU X1, X5
	PXOR X2, X5
	PXOR X4, X3
	PAND ·h264DeblockPB1(SB), X5
	PAVGB X0, X3
	PXOR X1, X4
	PAVGB ·h264DeblockPB3(SB), X3
	PAVGB X2, X4
	PAVGB X5, X3
	MOVOU ·h264DeblockPBA1(SB), X6
	PADDUSB X4, X3
	PSUBUSB X3, X6
	PSUBUSB ·h264DeblockPBA1(SB), X3
	PMINUB X7, X6
	PMINUB X7, X3
	PSUBUSB X6, X1
	PSUBUSB X3, X2
	PADDUSB X3, X1
	PADDUSB X6, X2

	MOVOU X1, (R9)(SI*2)
	MOVOU X2, (DI)
luma8_sse2_ret:
	RET

// func h264HLoopFilterLuma8ASM(pix *uint8, stride int, alpha int32, beta int32, tc0 *int8)
TEXT ·h264HLoopFilterLuma8ASM(SB), NOSPLIT, $96-32
	MOVQ pix+0(FP), R11
	MOVQ stride+8(FP), R12
	MOVL alpha+16(FP), AX
	TESTL AX, AX
	JLE   luma8_h_ret
	MOVL beta+20(FP), BX
	TESTL BX, BX
	JLE   luma8_h_ret

	LEAQ 0(SP), R10
	MOVQ R12, R14
	LEAQ (R14)(R14*2), R15
	LEAQ -4(R11), R12
	LEAQ (R12)(R15*1), R13
	H_LUMA8_TRANSPOSE6x8(0, 16, 32, 48, 64, 80)
	LEAQ (R12)(R14*8), R12
	LEAQ (R13)(R14*8), R13
	H_LUMA8_TRANSPOSE6x8(8, 24, 40, 56, 72, 88)

	LEAQ 48(SP), DI
	MOVQ $16, SI
	MOVL alpha+16(FP), AX
	MOVL beta+20(FP), BX
	MOVQ tc0+24(FP), CX
	CALL ·h264Luma8SSE2Core(SB)

	LEAQ -2(R11), R12
	LEAQ (R12)(R15*1), R13
	H_LUMA8_STORE8x4(16, 32, 48, 64)
	LEAQ (R12)(R14*8), R12
	LEAQ (R13)(R14*8), R13
	H_LUMA8_STORE8x4(24, 40, 56, 72)
luma8_h_ret:
	RET

// h264Chroma8SSE2Core filters 8 8-bit chroma samples with X0=p1,
// X1=p0, X2=q0, X3=q1, AX=alpha, BX=beta, CX=tc0, and DX=tc0 repeat count.
TEXT ·h264Chroma8SSE2Core(SB), NOSPLIT, $0-0
	TESTL AX, AX
	JLE   chroma8_sse2_ret
	TESTL BX, BX
	JLE   chroma8_sse2_ret
	DECL AX
	DECL BX

	MOVD AX, X4
	PSHUFLW $0, X4, X4
	PUNPCKLQDQ X4, X4
	PACKUSWB X4, X4        // alpha - 1
	MOVD BX, X5
	PSHUFLW $0, X5, X5
	PUNPCKLQDQ X5, X5
	PACKUSWB X5, X5        // beta - 1

	MOVOU X2, X6
	MOVOU X1, X7
	PSUBUSB X1, X6
	PSUBUSB X2, X7
	POR X6, X7
	PSUBUSB X4, X7

	MOVOU X1, X6
	MOVOU X0, X4
	PSUBUSB X0, X6
	PSUBUSB X1, X4
	POR X6, X4
	PSUBUSB X5, X4
	POR X4, X7

	MOVOU X2, X6
	MOVOU X3, X4
	PSUBUSB X3, X6
	PSUBUSB X2, X4
	POR X6, X4
	PSUBUSB X5, X4
	POR X4, X7
	PXOR X6, X6
	PCMPEQB X6, X7        // base mask

	MOVD (CX), X6
	PUNPCKLBW X6, X6
	CMPL DX, $2
	JNE  chroma8_tc_ready
	PUNPCKLBW X6, X6
chroma8_tc_ready:
	PAND X6, X7           // masked tc

	PCMPEQB X4, X4
	MOVOU X1, X5
	PXOR X2, X5
	PXOR X4, X3
	PAND ·h264DeblockPB1(SB), X5
	PAVGB X0, X3
	PXOR X1, X4
	PAVGB ·h264DeblockPB3(SB), X3
	PAVGB X2, X4
	PAVGB X5, X3
	MOVOU ·h264DeblockPBA1(SB), X6
	PADDUSB X4, X3
	PSUBUSB X3, X6
	PSUBUSB ·h264DeblockPBA1(SB), X3
	PMINUB X7, X6
	PMINUB X7, X3
	PSUBUSB X6, X1
	PSUBUSB X3, X2
	PADDUSB X3, X1
	PADDUSB X6, X2
chroma8_sse2_ret:
	RET

