func TestFFmpegFrameMD5OracleTestsrc32BFrames(t *testing.T) {
	if os.Getenv("GOH264_ORACLE") != "1" {
		t.Skip("set GOH264_ORACLE=1 to run native ffmpeg oracle")
	}
	if _, err := exec.LookPath("ffmpeg"); err != nil {
		t.Skip("ffmpeg not available")
	}
	for _, tt := range bFrameFixtureCases() {
		t.Run(tt.name, func(t *testing.T) {
			path := writeTempH264(t, decodeHexFixture(t, tt.hex))
			cmd := exec.Command("ffmpeg",
				"-v", "error",
				"-i", path,
				"-an", "-sn", "-dn",
				"-f", "framemd5",
				"-",
			)
			got, err := cmd.CombinedOutput()
			if err != nil {
				t.Fatalf("ffmpeg framemd5: %v\n%s", err, got)
			}
			out := string(got)
			for i, md5 := range tt.want {
				line := fmt.Sprintf("0,          %d,          %d,        1,     1536, %s", i, i, md5)
				if !strings.Contains(out, line) {
					t.Fatalf("frame[%d] missing %q in framemd5:\n%s", i, line, out)
				}
			}
		})
	}
}

