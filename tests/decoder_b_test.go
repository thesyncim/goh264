func TestDecodePacketSideDataFollowsDelayedBFrames(t *testing.T) {
	data := decodeHexFixture(t, testsrc16CAVLCBFramesAnnexBHex)
	config, samples := annexBToAVCConfigAndSamples(t, data, 4)
	if len(samples) != 3 {
		t.Fatalf("samples = %d, want 3", len(samples))
	}

	dec := NewDecoder()
	if _, err := dec.ParseAVCDecoderConfigurationRecord(config); err != nil {
		t.Fatal(err)
	}
	var frames []*Frame
	for i, sample := range samples {
		pkt := Packet{
			Data: sample,
			SideData: []PacketSideData{
				{
					Type: PacketSideDataA53ClosedCaptions,
					Data: []byte{byte(i + 1)},
				},
				{
					Type: PacketSideDataContentLightLevel,
					Data: decoderPacketContentLightSideData(uint32(100+i), uint32(10+i)),
				},
				{
					Type: PacketSideDataDisplayMatrix,
					Data: decoderPacketDisplayMatrixSideData([9]int32{int32(i + 1), 0, 0, 0, 65536, 0, 0, 0, 1 << 30}),
				},
				{
					Type: PacketSideDataDynamicHDR10Plus,
					Data: []byte{0xd0, byte(i + 1), 0x80},
				},
				{
					Type: PacketSideDataLCEVC,
					Data: []byte{0xe0, byte(i + 1), 0x00, 0x03, 0x01},
				},
			},
		}
		out, err := dec.DecodePacketFrames(pkt)
		if err != nil {
			t.Fatalf("sample[%d]: %v", i, err)
		}
		for sideIndex := range pkt.SideData {
			for byteIndex := range pkt.SideData[sideIndex].Data {
				pkt.SideData[sideIndex].Data[byteIndex] = 0xff
			}
		}
		frames = append(frames, out...)
	}
	out, err := dec.DecodePacketFrames(Packet{
		SideData: []PacketSideData{
			{Type: PacketSideDataA53ClosedCaptions, Data: []byte{0xfe}},
			{Type: PacketSideDataContentLightLevel, Data: decoderPacketContentLightSideData(999, 99)},
			{Type: PacketSideDataDisplayMatrix, Data: decoderPacketDisplayMatrixSideData([9]int32{0xfe, 0, 0, 0, 65536, 0, 0, 0, 1 << 30})},
			{Type: PacketSideDataDynamicHDR10Plus, Data: []byte{0xfe, 0xed}},
			{Type: PacketSideDataLCEVC, Data: []byte{0xba, 0xad}},
		},
	})
	if err != nil {
		t.Fatalf("empty packet flush with side data: %v", err)
	}
	frames = append(frames, out...)
	assertFrameMD5Strings(t, frames, []string{
		"4296e3dc95829cc27071a8685a428494",
		"36f5a9b9064709ee891652e8f4e06992",
		"aa778b981f96d21489196f6a0faa0959",
	})

	want := [][]byte{{0x01}, {0x03}, {0x02}}
	wantCLL := []uint32{100, 102, 101}
	wantHDR10Plus := [][]byte{{0xd0, 0x01, 0x80}, {0xd0, 0x03, 0x80}, {0xd0, 0x02, 0x80}}
	wantLCEVC := [][]byte{{0xe0, 0x01, 0x00, 0x03, 0x01}, {0xe0, 0x03, 0x00, 0x03, 0x01}, {0xe0, 0x02, 0x00, 0x03, 0x01}}
	if len(frames) != len(want) {
		t.Fatalf("frames = %d, want %d", len(frames), len(want))
	}
	for i, frame := range frames {
		if got := frame.SideData.A53ClosedCaptions; !bytes.Equal(got, want[i]) {
			t.Fatalf("frame[%d] packet a53 = %x, want %x", i, got, want[i])
		}
		if frame.SideData.ContentLight == nil || frame.SideData.ContentLight.MaxContentLightLevel != wantCLL[i] {
			t.Fatalf("frame[%d] packet content light = %+v, want max CLL %d", i, frame.SideData.ContentLight, wantCLL[i])
		}
		if frame.SideData.DisplayOrientation == nil || frame.SideData.DisplayOrientation.Matrix[0] != int32(want[i][0]) {
			t.Fatalf("frame[%d] packet display matrix = %+v", i, frame.SideData.DisplayOrientation)
		}
		if got := frame.SideData.DynamicHDR10Plus; !bytes.Equal(got, wantHDR10Plus[i]) {
			t.Fatalf("frame[%d] packet dynamic hdr10+ = %x, want %x", i, got, wantHDR10Plus[i])
		}
		if got := frame.SideData.LCEVC; !bytes.Equal(got, wantLCEVC[i]) {
			t.Fatalf("frame[%d] packet lcevc = %x, want %x", i, got, wantLCEVC[i])
		}
	}
}

